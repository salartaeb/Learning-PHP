<?php
$string = "Hello World";

// make string lowercase example
echo strtolower($string);

// make string uppercase example
echo strtoupper($string);

// find string length
echo strlen($string);

// print out index of string
echo $string[0];

// replace part of string
echo str_replace("Hello", "Bye", $string);

// get substring of a string
echo substr($string, 6, 3);

